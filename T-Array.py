class Satelit:
    def __init__(self, nama, tahun_peluncuran, jenis, massa):
        self.nama = nama
        self.tahun_peluncuran = tahun_peluncuran
        self.jenis = jenis
        self.massa = massa

    def __str__(self):
        return f"Satelit {self.nama} ({self.jenis}), diluncurkan pada tahun {self.tahun_peluncuran}, dengan massa {self.massa} kg"

class Array(object):
    def __init__(self) -> None:
        self._internal = 10*[None]

    def __getitem__(self, key):
        return self._internal[key]
    
    def __setitem__(self, key, value):
        if key >= len(self._internal):
            kurang = key - len(self._internal) + 1
            li = kurang * [None]
            self._internal.extend(li)
        self._internal[key] = value
       
